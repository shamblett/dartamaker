  int _numchars = 1;

  /// Emojii's list
  List<String> get emojiis => _emojiis;

  /// Default
  DartamakerPluginEmojii(String? numchars) {
    if (numchars != null) {
      final val = int.tryParse(numchars);
      if (val != null && val > 0) {
        _numchars = val;
      }
    }
  }

  @override
  String apply() {
    final str = StringBuffer();
    for (var i = 0; i < _numchars; i++) {
      str.write(_emojiis[Random().nextInt(_emojiis.length - 1)]);
    }
    return str.toString();
  }
